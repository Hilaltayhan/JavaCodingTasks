package nurdan.week1;

public class OddOrEven {

    public static void main(String[] args) {

        OddOrEven(5);
    }

    public static void OddOrEven(int number){

        if(number % 2 == 0){
            System.out.println(number + " is Even" );
        }else{
            System.out.println(number + " is Odd");
        }
    }
}

