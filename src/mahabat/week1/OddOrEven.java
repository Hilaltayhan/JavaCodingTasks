package mahabat.week1;

public class OddOrEven {

    public static void main(String[] args) {
        int number = 6; // Change this to any number you want to check

        if (number % 2 == 0) {
            System.out.println(number + " is even.");
        } else {
            System.out.println(number + " is odd.");
        }
    }


}
